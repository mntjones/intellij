package academy.learnprogramming;

public class Vehicle {

    private String engine;
    private int weight;
    private double length;
    private  String maker;
    private String model;
    private int maxSpeed;

    public Vehicle(String engine, int weight, double length, String maker, String model, int maxSpeed) {
        this.engine = engine;
        this.weight = weight;
        this.length = length;
        this.maker = maker;
        this.model = model;
        this.maxSpeed = maxSpeed;
    }



}
